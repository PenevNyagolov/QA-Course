public class Age {
    public static void main(String[] args) {
        int myAge = 20;
        int mySisterAge = 25;

        if (myAge == mySisterAge){
            System.out.println("Me and my sister are the same age");
        }else{
            if (myAge > mySisterAge){
                System.out.println("I`m older than my sister");
            }else{
                System.out.println("I`m younger than my sister");
            }
        }
    }
}
